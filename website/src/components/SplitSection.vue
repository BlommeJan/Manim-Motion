function renderCode() {
  const el = document.getElementById('code-display')
  if (!el) return
  el.innerHTML = ''
  codeLines.forEach((line, i) => {
    const row = document.createElement('div')
    row.className = 'code-line'
    row.style.opacity = '0'
    row.style.animation = `codeType 0.05s ${i * 0.04}s forwards`
    const ln = document.createElement('span')
    ln.className = 'ln'; ln.textContent = String(i + 1).padStart(2, ' ')
    row.appendChild(ln)
    const content = document.createElement('span')
    line.tokens.forEach(tok => {
      const span = document.createElement('span')
      span.className = tok.t ? `code-${tok.t}` : 'code-var'
      span.textContent = tok.v
      content.appendChild(span)
    })
    row.appendChild(content)
    el.appendChild(row)
  })
}

